// ============================================================================
// ابزار ساخت slug (بخش انگلیسیِ لینک اختصاصی)
// ============================================================================
// مثال: "Backend Developer"        → "backend-developer"
//        "برنامه‌نویس Backend"      → "backend"        (فقط بخش لاتین نگه داشته می‌شود)
//        "کارشناس منابع انسانی"     → "job"           (اگر لاتین نبود، مقدار پیش‌فرض)
//
// چون لینک باید کوتاه و خوانا باشد (company.com/apply/backend-developer)،
// فقط حروف/اعداد لاتین را نگه می‌داریم و فاصله‌ها را به خط تیره تبدیل می‌کنیم.
// یکتا بودن slug در کنترلر با افزودن شماره (backend-developer-2) تضمین می‌شود.
// ============================================================================

/**
 * تبدیل یک متن به slug ساده و امن برای URL.
 * @param {string} text
 * @returns {string}
 */
function slugify(text) {
  if (!text) return "job";

  const slug = String(text)
    .toLowerCase()
    .trim()
    // هر چیزی غیر از حروف/اعداد لاتین را به فاصله تبدیل کن
    .replace(/[^a-z0-9]+/g, " ")
    .trim()
    // فاصله‌ها را به خط تیره تبدیل کن
    .replace(/\s+/g, "-");

  // اگر بعد از پاک‌سازی چیزی نماند (مثلاً عنوان کاملاً فارسی بود)
  return slug || "job";
}

module.exports = { slugify };
